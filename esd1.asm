LIST P=18F458
#include <P18F458.INC>

CONFIG OSC = INTIO67
CONFIG WDT = OFF
CONFIG LVP = OFF

ORG 0x00
GOTO MAIN

MAIN:

MOVLW 0x72
MOVWF OSCCON

MOVLW 0x0F
MOVWF ADCON1

; RA0, RA1 INPUT
BSF TRISA,0
BSF TRISA,1

; PORTD OUTPUT
CLRF TRISD

LOOP:

;========================
; BOTH BLACK -> STOP
;========================

BTFSC PORTA,0
GOTO CHECK_FORWARD

BTFSC PORTA,1
GOTO LEFT_BLACK

; STOP MOTORS
BCF PORTD,0
BCF PORTD,1
BCF PORTD,2
BCF PORTD,3

GOTO LOOP

;========================
; CHECK FORWARD
;========================

CHECK_FORWARD:

BTFSS PORTA,1
GOTO RIGHT_BLACK

;========================
; BOTH WHITE -> FORWARD
;========================

; LEFT MOTOR
BSF PORTD,0
BCF PORTD,1

; RIGHT MOTOR
BSF PORTD,2
BCF PORTD,3

GOTO LOOP

;========================
; LEFT BLACK -> LEFT TURN
;========================

LEFT_BLACK:

; LEFT MOTOR STOP
BCF PORTD,0
BCF PORTD,1

; RIGHT MOTOR FORWARD
BSF PORTD,2
BCF PORTD,3

GOTO LOOP

;========================
; RIGHT BLACK -> RIGHT TURN
;========================

RIGHT_BLACK:

; LEFT MOTOR FORWARD
BSF PORTD,0
BCF PORTD,1

; RIGHT MOTOR STOP
BCF PORTD,2
BCF PORTD,3

GOTO LOOP

END